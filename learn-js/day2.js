// First things first:
console.log("Hello World!");

// Alright, let's declare some variables:
var a = 1;
console.log(a);

// With var we can redeclare vars more than once:
var a = 2;
console.log(a);

// If we do not want to redeclare a var more than once we can use 'let':
let b = 3;
console.log(b);

